var number1 = Number(prompt('enter numer1: '))
var Operation = prompt('enter Operation: ')
var number2 = Number(prompt('enter number2: '))

if (Operation == '+') {
    alert(number1 + number2)
} else if (Operation == '-') {
    alert(number1 - number2)
} else if (Operation == '*') {
    alert(number1 * number2)
} else if (Operation == '/') {
    alert(number1/number2)
}