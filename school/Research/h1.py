while True:
    x=input('* / + - Power:')
    n1=int(input(': '))
    n2=int(input(': '))
    if x=='*':
        print(n1*n2)
    if x=='/':
        print(n1/n2)
    if x=='-':
        print(n1-n2)
    if x=='+':
        print(n1+n2)
    if x=='Power':
        print(n1**n2)
    
    
